def latest_status():
    """Print latest bucho's tweet.
    """
    url = urllib.urlopen('http://twitter.com/statuses/user_timeline/torufurukawa.json')
    tof = json.loads(url.read())
    print(tof[0]['text'])

def all_status():
    """Print all bucho's tweet.
    """
    url = urllib.urlopen('http://twitter.com/statuses/user_timeline/torufurukawa.json')
    tof = json.loads(url.read())
    for t in tof:
        print(t['text'])

